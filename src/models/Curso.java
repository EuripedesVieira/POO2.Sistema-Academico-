package models;

public class Curso {
	private int idCurso;
	private String nomeCurso;

	
	public int getIdCurso() {
		return idCurso;
	}

	public void setIdCurso(int idCurso) {
		this.idCurso = idCurso;
	}
	
	public String getNome() {
		return nomeCurso;
	}
	
	public void setNome(String nomeCurso) {
		this.nomeCurso = nomeCurso;
	}
}
